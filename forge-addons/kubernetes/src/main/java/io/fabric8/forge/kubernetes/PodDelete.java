package io.fabric8.forge.kubernetes;

import io.fabric8.kubernetes.api.model.PodSchema;
import org.jboss.forge.addon.ui.context.UIContext;
import org.jboss.forge.addon.ui.metadata.UICommandMetadata;
import org.jboss.forge.addon.ui.util.Categories;
import org.jboss.forge.addon.ui.util.Metadata;

/**
 * Command to delete a pod in kubernetes
 */
public class PodDelete extends AbstractPodCommand {


    @Override
    public UICommandMetadata getMetadata(UIContext context) {
        return Metadata.from(super.getMetadata(context), getClass())
                .category(Categories.create(CATEGORY))
                .name(CATEGORY + ": Pod Delete")
                .description("Deletes the given pod from the kubernetes cloud");
    }

    @Override
    protected void executePod(PodSchema pod, String podId) throws Exception {
        System.out.println("Deleting pod " + podId);
        getKubernetes().deletePod(podId);
    }
}

